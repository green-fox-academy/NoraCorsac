using System;

namespace TwoNumbers
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 22;
            int b = 13;

            Console.WriteLine(a + b);
            Console.WriteLine(a - b);
            Console.WriteLine(a * b);
            Console.WriteLine(a / b);
            Console.WriteLine(a / 13.0);
            Console.WriteLine(a % b);
            Console.ReadLine();
        }
    }
}
